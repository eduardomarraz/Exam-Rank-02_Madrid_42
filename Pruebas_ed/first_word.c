#include <unistd.h>

void	first_word(char *a)
{
	int i;

	while(a[i] == ' ' || a[i] == '\t')
		i++;

	while(a[i] != '\0' && a[i] != ' ' && a[i] != '\t')
	{
		write(1, &a[i], 1);
		i++;
	}
	write(1, "\n", 1);

}

int	main()
{
	first_word("Abc	de fghij");
	return 0;
}
